#include "cub3d.h"

void u_print_map(char **map_temp)
{
	int i;

	i = 0;
	printf("=============\n");
	while(map_temp[i])
	{
		printf("|%s|\n",map_temp[i]);
		i++;
	}
	printf("=============\n");
}
