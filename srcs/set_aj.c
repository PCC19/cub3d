#include "cub3d.h"

void	set_aj(t_vars *v, int *ajx, int *ajy, int i)
{
	*ajx = -1;
	*ajy = -1;
	if (v->rays[i].is_dn)
		*ajy = 1;
	if (v->rays[i].is_ri)
		*ajx = 1;
}
