#include "libft.h"

void	*ft_memchr(const void *s, int c, size_t n)
{
	size_t	i;
	char	*str;

	str = (char *)s;
	i = 0;
	while (i < n && str[i] != c)
		i++;
	if (i == n)
		return (NULL);
	else
		return (&str[i]);
}
