#include "libft.h"

int	ft_isnum(const char *s)
{
	int out;
	int i;

	out = 1;
	i = 0;
	while (s[i] == ' ' && s[i] != 0)
		i++;
	while (s[i] != 0)
	{
		if (!ft_isdigit(s[i]))
			out = 0;
		i++;
	}
	return (out);
}
